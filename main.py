message_counter = 0


async def auto_reply(update: Update, context: ContextTypes.DEFAULT_TYPE):
    global message_counter

    if not update.message or not update.message.text:
        return

    if update.effective_chat.type not in ["group", "supergroup"]:
        return

    text = update.message.text.lower()
    message_counter += 1

    if message_counter % 10 != 0:
        return

    if "смех" in text:
        await update.message.reply_text("Смех уже рядом, прячьтесь)")
    elif "бус" in text:
        await update.message.reply_text("🚐 *ускорился*", parse_mode="Markdown")
    elif "полиция" in text:
        await update.message.reply_text("Я вне юрисдикции 😅")
    elif "тцк" in text:
        await update.message.reply_text("Я этого не слышал 👀")
